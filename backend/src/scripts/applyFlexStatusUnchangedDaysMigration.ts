import { closeDatabasePool, pool } from "../config/database.js";

const sqlQueries = [
    `ALTER TABLE daily_call_plan_report_rows ADD COLUMN IF NOT EXISTS flex_status_unchanged_days INTEGER;`
];

async function run(): Promise<void> {
  const client = await pool.connect();

  try {
    for (const query of sqlQueries) {
      await client.query(query);
    }
    console.log("Applied migration 019_flex_status_unchanged_days.sql");
  } catch (error) {
    console.error("Migration failed:", error);
    throw error;
  } finally {
    client.release();
  }
}

run()
  .catch((error: unknown) => {
    console.error(error);
    process.exitCode = 1;
  })
  .finally(() => {
    void closeDatabasePool();
  });
